class Metadata(BaseModel):
    authors: List[str]
    license: str
    source: str
    description: str
    thumbnail: Optional[str] = ""
    spatial_bbox: List[float] = [0, 0, 1, 1]
    temporal_interval: List[str] = ["2020-01-01T00:00:00Z", "2020-01-02T00:00:00Z"]

    # validate source is a URL
    @validator("source")
    def source_is_url(cls, v):
        if not v.startswith("http") and not v.startswith("https"):
            raise ValueError("source must be a URL")
        return v

    # validate thumbnail is a url
    @validator("thumbnail")
    def thumbnail_is_url(cls, v):
        if not v:
            v = random.choice(links)
        if not v.startswith("http") and not v.startswith("https"):
            raise ValueError("thumbnail must be a URL")
        return v
